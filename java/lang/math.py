import math


class Math:
    @staticmethod
    def abs(val):
        return abs(val)

    @staticmethod
    def max(a, b):
        return max(a, b)

    @staticmethod
    def min(a, b):
        return min(a, b)

    @staticmethod
    def pow(a, b):
        return math.pow(a, b)

    @staticmethod
    def sqrt(val):
        return math.sqrt(val)
